#include "libft.h"

int			ft_bzero(void *s, size_t n)
{
	size_t			i1;
	unsigned char	*ps;

	i1 = 0;
	ps = (unsigned char*)s;
	if (n == 0)
	{
		return (1);
	}
	while (i1 < n)
	{
		*ps = '\0';
		i1++;
		ps++;
	}
	return (1);
}
